public class Operaciones {
	/**
	 * 
	 * @param n1 primer valor que se ingresara
	 * @param n2 segundo valor que se utiliza al ser ingresado
	 * @return el valor resultado dependiendo de cada operacion
	 */
	public int sumar(int n1, int n2){
		return n1 + n2;	
	}
	public int restar(int n1, int n2){
		return n1 - n2;	
	}
	public int multiplicar(int n1, int n2){
		return n1 * n2;	
	}
	public int dividir(int n1, int n2){
		return n1 / n2;	
	}

}
